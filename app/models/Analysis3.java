public class Analysis3 {

    int spot;
    int frequency;

    public Analysis3(int spot, int frequency) {
        this.spot = spot;
        this.frequency = frequency;
    }

    public int getSpot() {
        return spot;
    }

    public void setSpot(int spot) {
        this.spot = spot;
    }

    public int getFrequency() {
        return frequency;
    }

    public void setFrequency(int frequency) {
        this.frequency = frequency;
    }
}